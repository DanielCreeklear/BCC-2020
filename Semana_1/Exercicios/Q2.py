'''
Escreva instruções para converter a temperatura de "C" graus Celsius para Fahrenheit 
sabendo que temos que multiplicar C por 9/5 e depois somar 32.  
O resultado deve ser armazenado na variável "F".
Imprima o resultado da seguinte forma "?? graus Celsius corresponde a ?? graus Fahrenheit".
O programa deve imprimir respostas para duas temperaturas:
C = 55  e  C = 65
Assim, o resultado a ser exibido deve ser:
"55 graus Celsius corresponde a 131.0 graus Fahrenheit".
"65 graus Celsius corresponde a 149.0 graus Fahrenheit".
'''
C = 55
F = C * 9/5 + 32
print(C,'graus Celsius corresponde a',F,'graus Fahrenheit')
C = 65
F = C * 9/5 + 32
print(C,'graus Celsius corresponde a',F,'graus Fahrenheit')